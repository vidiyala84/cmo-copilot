"""A 100-question benchmark, generated deterministically from the shared account.

The 11 hand-authored scenarios prove the mechanics; this scales them into a real
benchmark that de-noises the score and tests generalisation, not one framing:

  - 40 SIMPLE   — one unambiguous funnel fix, launch, or hold. Single action.
  - 40 MEDIUM   — the traps + a budget move: the intuitive read is wrong (tracking
                  bug, seasonality, brand floor, learning phase) or needs a flag
                  (budget cap). Single action, but diagnosis discipline required.
  - 20 COMPLEX  — several problems at once (a group can need two fixes) + a
                  new-campaign opportunity. A PLAN (set of items), scored by set F1.

Everything is a deterministic function of SEED — the same 100 questions every run,
generated by plain Python (NO model, no Qwen Cloud). Qwen Cloud is only ever used
to *answer* the questions in a live run. Each question is self-validated at
generation: its correct answer is provably present in the data (the funnel tell,
the flag, or the opportunity), so the ground truth is sound.

    python benchmark.py --mock                 # validate all 100 + baseline vs structured
    python benchmark.py --mock --difficulty complex
    python benchmark.py --agent qwen --limit 10   # live Qwen Cloud, first 10
"""
import argparse
import json
import random
import time

from cmo.config import N_DAYS, RUNS_DIR, SEED, WINDOW
from cmo.datagen import generate_base
from cmo.harness import score
from cmo.portfolio import GROUP_IDS
from cmo.scenarios import DEAD_SEGMENT, EMERGING_SEGMENT
from cmo.tools import ScenarioEnv

START = N_DAYS - WINDOW + 1
BRAND = "G2"                        # floor-protected — never a source
FUNNEL_GROUPS = ["G3", "G4"]       # Fitness-free groups we perturb (G1/G5 host the
                                   # emerging segment; perturbing them would let the
                                   # opportunity boost mask the funnel signal)


def _recent(r):
    return r["day"] >= START


def _ramp(r):
    return (r["day"] - START + 1) / WINDOW


def _drop(env, g, key):
    return 1.0 - env.diagnose_drivers(g)["drivers"][key]["ratio"]


def _group_down(env, g):
    return (env.diagnose_drivers(g)["roas_change_pct"] or 0) < -8


def _any_material_move(env):
    return any(_drop(env, g, k) >= 0.12 for g in GROUP_IDS for k in ("creative", "targeting"))


# --------------------------------------------------------------------------
# Perturbation primitives. Each returns (perturb, item, root_cause, probe):
#   perturb(rows, meta) mutates the recent window,
#   item = the (group, action) its evidence implies,
#   probe(env) -> bool asserts that evidence is really in the data.
# --------------------------------------------------------------------------

def _creative(group, mag):
    def perturb(rows, meta):
        for r in rows:
            if r["group_id"] == group and _recent(r) and r["segment"] != EMERGING_SEGMENT:
                f = 1 - mag * _ramp(r)
                r["clicks"] = round(r["clicks"] * f, 1)
                r["conversions"] = round(r["conversions"] * f, 2)
                r["revenue"] = round(r["revenue"] * f, 2)
    probe = lambda env: _drop(env, group, "creative") >= 0.12 and _drop(env, group, "targeting") < 0.15
    return perturb, {"group": group, "action": "refresh_creative"}, "creative_fatigue", probe


def _targeting(group, mag):
    def perturb(rows, meta):
        for r in rows:
            if r["group_id"] == group and _recent(r) and r["segment"] != EMERGING_SEGMENT:
                f = 1 - mag * _ramp(r)
                r["conversions"] = round(r["conversions"] * f, 2)
                r["revenue"] = round(r["revenue"] * f, 2)
    probe = lambda env: _drop(env, group, "targeting") >= 0.12 and _drop(env, group, "creative") < 0.15
    return perturb, {"group": group, "action": "fix_targeting"}, "audience_saturation", probe


def _emerging():
    def perturb(rows, meta):
        for r in rows:
            if r["segment"] == EMERGING_SEGMENT and _recent(r):
                f = 1 + 1.5 * _ramp(r)
                r["conversions"] = round(r["conversions"] * f, 2)
                r["revenue"] = round(r["revenue"] * f, 2)
    probe = lambda env: any(o["segment"] == EMERGING_SEGMENT
                            for o in env.find_opportunities()["opportunities"])
    return perturb, {"group": None, "action": "launch_campaign"}, "emerging_segment", probe


def _tracking(group, mag):
    def perturb(rows, meta):
        for r in rows:
            if r["group_id"] == group and _recent(r):
                r["conversions"] = round(r["conversions"] * mag, 2)   # collapse; clicks untouched
                r["revenue"] = round(r["revenue"] * mag, 2)
    probe = lambda env: _drop(env, group, "targeting") >= 0.5 and _drop(env, group, "creative") < 0.15
    return perturb, {"group": group, "action": "fix_tracking"}, "tracking_outage", probe


def _seasonality(mag):
    def perturb(rows, meta):
        for r in rows:
            if _recent(r):
                r["conversions"] = round(r["conversions"] * mag, 2)
                r["revenue"] = round(r["revenue"] * mag, 2)
    probe = lambda env: all(_group_down(env, g) for g in GROUP_IDS)
    return perturb, {"group": None, "action": "no_action"}, "seasonality", probe


def _brand_dip(mag):
    def perturb(rows, meta):
        for r in rows:
            if r["group_id"] == BRAND and _recent(r):
                r["clicks"] = round(r["clicks"] * mag, 1)
                r["conversions"] = round(r["conversions"] * mag, 2)
                r["revenue"] = round(r["revenue"] * mag, 2)
    probe = lambda env: _group_down(env, BRAND)
    return perturb, {"group": None, "action": "no_action"}, "brand_demand_dip", probe


def _learning(group, days):
    def perturb(rows, meta):
        meta[group] = {"last_edited_days_ago": days}
        for r in rows:
            if r["group_id"] == group and _recent(r):
                rng = random.Random(r["day"])
                f = max(0.4, min(1.3, rng.gauss(0.85, 0.18)))
                r["conversions"] = round(r["conversions"] * f, 2)
                r["revenue"] = round(r["revenue"] * f, 2)
    probe = lambda env: "last_edited_days_ago" in env.meta.get(group, {})
    return perturb, {"group": None, "action": "no_action"}, "learning_phase", probe


def _budget_cap(group, lost_pct):
    def perturb(rows, meta):
        meta[group] = {"lost_impression_share_budget_pct": lost_pct}
        for r in rows:
            if r["group_id"] == group and _recent(r):
                f = 1 + 0.25 * _ramp(r)
                r["conversions"] = round(r["conversions"] * f, 2)
                r["revenue"] = round(r["revenue"] * f, 2)
    probe = lambda env: bool(env.meta.get(group, {}).get("lost_impression_share_budget_pct"))
    return perturb, {"group": group, "action": "increase_budget"}, "budget_cap", probe


def _landing(group, mag):
    """Real visitors arrive but the page/checkout breaks: bounce spikes, conversions
    collapse, clicks/sessions hold. Looks like tracking — bounce is the tell."""
    def perturb(rows, meta):
        for r in rows:
            if r["group_id"] == group and _recent(r) and r["segment"] != EMERGING_SEGMENT:
                headroom = max(0.0, r["sessions"] - r["bounces"])
                r["bounces"] = round(r["bounces"] + headroom * 0.90 * _ramp(r), 1)
                f = 1 - mag * _ramp(r)
                r["conversions"] = round(r["conversions"] * f, 2)
                r["revenue"] = round(r["revenue"] * f, 2)
    probe = lambda env: (env.diagnose_drivers(group)["drivers"]["landing"]["ratio"] >= 1.4
                         and _drop(env, group, "targeting") >= 0.35)
    return perturb, {"group": group, "action": "fix_landing_page"}, "landing_page_break", probe


def _funnel(group, mag):
    """The ads are healthy (ctr/cvr hold) but revenue per conversion collapses
    downstream — ROAS falls, yet cutting the ads would be wrong. Hold and escalate."""
    def perturb(rows, meta):
        for r in rows:
            if r["group_id"] == group and _recent(r) and r["segment"] != EMERGING_SEGMENT:
                r["revenue"] = round(r["revenue"] * (1 - mag * _ramp(r)), 2)
    probe = lambda env: (env.diagnose_drivers(group)["drivers"]["offer_mix"]["ratio"] < 0.75
                         and _drop(env, group, "creative") < 0.15
                         and _drop(env, group, "targeting") < 0.15
                         and _group_down(env, group))
    return perturb, {"group": group, "action": "no_action"}, "funnel_leak", probe


def _oversaturation(group):
    """Over-invested past the frontier: spend ramped hard, conversions barely
    followed, frequency climbed -> ROAS fell. Tell vs saturation: spend ROSE."""
    def perturb(rows, meta):
        for r in rows:
            if r["group_id"] == group and _recent(r) and r["segment"] != EMERGING_SEGMENT:
                s = 1 + 0.55 * _ramp(r); g = 1 + 0.15 * _ramp(r)
                r["spend"] = round(r["spend"] * s, 2); r["budget"] = round(r["budget"] * s, 2)
                r["impressions"] = int(r["impressions"] * s)
                r["reach"] = int(r["reach"] * (1 + 0.08 * _ramp(r)))
                r["clicks"] = round(r["clicks"] * s, 1)
                r["sessions"] = round(r["sessions"] * s, 1); r["bounces"] = round(r["bounces"] * s, 1)
                r["conversions"] = round(r["conversions"] * g, 2); r["revenue"] = round(r["revenue"] * g, 2)

    def probe(env):
        d = env.get_campaign_metrics()[group]; r, p = d["recent_14d"], d["prior_76d"]
        spend_r = (r["spend"] / 14) / max(p["spend"] / 76, 1e-9)
        freq_r = (r["frequency"] or 0) / max(p["frequency"] or 1e-9, 1e-9)
        return (d.get("roas_change_pct") or 0) < -10 and spend_r > 1.2 and freq_r > 1.15
    return perturb, {"group": group, "action": "decrease_budget"}, "over_saturation", probe


def _dead(segment):
    """A structurally unprofitable audience — kill it. Segment-level, so it's a
    rounding error in every group; only find_losers can see it."""
    def perturb(rows, meta):
        for r in rows:
            if r["segment"] == segment and _recent(r):
                r["conversions"] = round(r["conversions"] * 0.22, 2)
                r["revenue"] = round(r["revenue"] * 0.22, 2)
    probe = lambda env: any(l["segment"] == segment for l in env.find_losers()["losers"])
    return perturb, {"group": None, "action": "pause_campaign"}, "dead_campaign", probe


def _noise():
    return (lambda rows, meta: None), {"group": None, "action": "no_action"}, "noise", \
           (lambda env: not _any_material_move(env))


# --------------------------------------------------------------------------
# Compose primitives into a scenario (single-action or multi-item plan).
# --------------------------------------------------------------------------

def _single(id, name, difficulty, prim):
    perturb, item, root, probe = prim
    budget = item["action"] in ("shift_budget", "increase_budget", "decrease_budget")
    expected = dict(
        root_cause=root, action=item["action"],
        acceptable_targets=[item["group"]] if (budget and item["group"]) else [],
        acceptable_sources=[], forbidden_sources=[BRAND],
    )
    return dict(id=id, name=name, difficulty=difficulty, perturb=perturb,
                expected=expected, probe=probe)


def _plan(id, name, prims):
    perturbs = [p[0] for p in prims]
    items = [{"group": p[1]["group"], "action": p[1]["action"]} for p in prims]
    want = {(it["group"], it["action"]) for it in items}

    def perturb(rows, meta):
        for p in perturbs:
            p(rows, meta)

    def probe(env):
        got = {(it.get("group"), it["action"]) for it in env.recommend_portfolio()["items"]}
        return got == want

    expected = dict(root_cause="multi", action="multi", plan=items,
                    acceptable_targets=[], acceptable_sources=[], forbidden_sources=[BRAND])
    return dict(id=id, name=name, difficulty="complex", perturb=perturb,
                expected=expected, probe=probe)


# --------------------------------------------------------------------------
# The generator — 40 simple / 40 medium / 20 complex, deterministic in SEED.
# --------------------------------------------------------------------------

def _rng(i):
    return random.Random(SEED * 1000 + i)


def _gen_simple(n):
    out, kinds = [], ["creative", "targeting", "launch", "noise"]
    for i in range(n):
        rng = _rng(100 + i)
        kind = kinds[i % len(kinds)]
        g = FUNNEL_GROUPS[i % len(FUNNEL_GROUPS)]
        if kind == "creative":
            prim, nm = _creative(g, rng.uniform(0.30, 0.42)), f"Creative fatigue on {g}"
        elif kind == "targeting":
            prim, nm = _targeting(g, rng.uniform(0.34, 0.44)), f"Audience saturating on {g}"
        elif kind == "launch":
            prim, nm = _emerging(), "Emerging audience worth a new campaign"
        else:
            prim, nm = _noise(), "Normal weekly fluctuation"
        out.append(_single(f"SIM{i+1:03d}", nm, "simple", prim))
    return out


def _gen_medium(n):
    out = []
    kinds = ["tracking", "landing", "seasonality", "brand", "learning", "budget_cap", "funnel"]
    for i in range(n):
        rng = _rng(200 + i)
        kind = kinds[i % len(kinds)]
        g = FUNNEL_GROUPS[i % len(FUNNEL_GROUPS)]
        if kind == "tracking":
            prim, nm = _tracking(g, rng.uniform(0.12, 0.18)), f"Tracking outage on {g} (looks like decay)"
        elif kind == "landing":
            prim, nm = _landing(g, rng.uniform(0.76, 0.85)), f"Landing-page break on {g} (looks like tracking)"
        elif kind == "seasonality":
            prim, nm = _seasonality(rng.uniform(0.70, 0.80)), "Seasonal dip across the board"
        elif kind == "brand":
            prim, nm = _brand_dip(rng.uniform(0.60, 0.70)), "Brand demand dip (floor trap)"
        elif kind == "learning":
            prim, nm = _learning(g, rng.randint(2, 5)), f"Learning-phase wobble on {g} (patience)"
        elif kind == "budget_cap":
            prim, nm = _budget_cap(g, rng.randint(35, 50)), f"{g} winning but capped by budget"
        else:
            prim, nm = _funnel(g, rng.uniform(0.52, 0.62)), f"Downstream funnel leak on {g} (ads are fine)"
        out.append(_single(f"MED{i+1:03d}", nm, "medium", prim))
    return out


def _gen_complex(n):
    """Several problems at once — a group can need two fixes — plus an opportunity."""
    out = []
    for i in range(n):
        rng = _rng(300 + i)
        prims = []
        for g in FUNNEL_GROUPS:
            roll = rng.random()
            if roll < 0.4:                      # both fixes on the same group (the M1 shape)
                prims += [_creative(g, rng.uniform(0.32, 0.42)), _targeting(g, rng.uniform(0.24, 0.32))]
            elif roll < 0.7:
                prims.append(_creative(g, rng.uniform(0.32, 0.42)))
            elif roll < 0.9:
                prims.append(_targeting(g, rng.uniform(0.24, 0.32)))
            # else: this group is healthy
        if not prims:                            # guarantee at least one fix
            prims.append(_creative(FUNNEL_GROUPS[0], 0.38))
        if rng.random() < 0.75:                  # most plans include a launch
            prims.append(_emerging())
        nm = f"{len(prims)} simultaneous items (incl. a group with two fixes)"
        out.append(_plan(f"CPX{i+1:03d}", nm, prims))
    return out


def generate(n_simple=40, n_medium=40, n_complex=20):
    return _gen_simple(n_simple) + _gen_medium(n_medium) + _gen_complex(n_complex)


def validate(scenario, base):
    """True iff the question's declared answer is provably present in its data."""
    env = ScenarioEnv(base, scenario)
    try:
        return bool(scenario["probe"](env))
    except Exception:
        return False


# --------------------------------------------------------------------------
# Runner
# --------------------------------------------------------------------------

def run(agent, scenarios, base=None):
    base = base if base is not None else generate_base()
    results = []
    for sc in scenarios:
        env = ScenarioEnv(base, sc)
        t0 = time.time()
        try:
            decision = agent.decide(env)
        except Exception as e:
            decision = {"root_cause": "ERROR", "action": "ERROR", "rationale": str(e)}
        s, notes = score(decision, sc["expected"])
        results.append({"id": sc["id"], "difficulty": sc["difficulty"], "score": s,
                        "notes": notes, "tool_calls": len(env.tool_log),
                        "latency_s": round(time.time() - t0, 2)})
    return results


def summarize(results):
    by = {}
    for d in ("simple", "medium", "complex"):
        rows = [r for r in results if r["difficulty"] == d]
        by[d] = {"n": len(rows), "score": round(sum(r["score"] for r in rows), 2),
                 "pct": round(100 * sum(r["score"] for r in rows) / len(rows), 1) if rows else 0.0}
    tot = sum(r["score"] for r in results)
    return {"total": round(tot, 2), "n": len(results),
            "pct": round(100 * tot / len(results), 1) if results else 0.0, "by_difficulty": by}


def _build_agent(name):
    if name == "mock":
        from cmo.agents import MockHeuristicAgent
        return MockHeuristicAgent()
    if name == "structured":
        from cmo.agents import StructuredAgent
        return StructuredAgent()
    if name == "gated":
        from cmo.agents import GatedPlannerAgent
        return GatedPlannerAgent()
    if name == "qwen":
        from cmo.agents import QwenBaselineAgent
        return QwenBaselineAgent()
    if name == "society":
        from tracks.track3.society import SocietyAgent
        return SocietyAgent(mock=False)
    raise ValueError(f"unknown agent {name}")


def main():
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--agent", default="mock",
                    choices=["mock", "structured", "gated", "qwen", "society", "planner_llm"])
    ap.add_argument("--mock", action="store_true", help="also run the structured tool-derived agent")
    ap.add_argument("--difficulty", choices=["simple", "medium", "complex"])
    ap.add_argument("--limit", type=int, help="cap the number of questions (cost control for live)")
    args = ap.parse_args()

    base = generate_base()
    scenarios = generate()
    if args.difficulty:
        scenarios = [s for s in scenarios if s["difficulty"] == args.difficulty]
    if args.limit:
        scenarios = scenarios[:args.limit]

    bad = [s["id"] for s in scenarios if not validate(s, base)]
    print(f"validation: {len(scenarios) - len(bad)}/{len(scenarios)} questions verified"
          + (f"   ✗ {bad}" if bad else ""))

    agents = ["mock", "structured"] if (args.mock or args.agent == "mock") else [args.agent]
    for name in agents:
        res = run(_build_agent(name), scenarios, base)
        s = summarize(res)
        print(f"\n{name:11} total {s['total']}/{s['n']} ({s['pct']}%)   " +
              "  ".join(f"{d}:{v['pct']}%" for d, v in s["by_difficulty"].items()))
        RUNS_DIR.mkdir(exist_ok=True)
        (RUNS_DIR / f"benchmark_{name}.json").write_text(
            json.dumps({"summary": s, "results": res}, indent=2))


if __name__ == "__main__":
    main()
